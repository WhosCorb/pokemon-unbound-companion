export function decodeGen3String(bytes: Uint8Array, maxLen: number): string {
  let result = "";
  for (let i = 0; i < maxLen; i++) {
    const byte = bytes[i];
    if (byte === 0xFF) break;
    result += GEN3_CHARS[byte] ?? "?";
  }
  return result;
}
